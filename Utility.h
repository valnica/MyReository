//////////////////////////////////////////////
// Name : Clamp
//
// Over View : 指定した範囲に制限する
//
// Argument : 最小値、最大値、 制限するデータ
//
// Return : 無し
//////////////////////////////////////////////
template <typename T>
void Clamp(T min, T max, T& data)
{
	if (data > max) data = max;
	else if (data < min) data = min;
}

//////////////////////////////////////////////
// Name : Swap
//
// Over View : データの入れ替え
//
// Argument : 入れ替えデータ１、入れ替えデータ２
//
// Return : 増し
//////////////////////////////////////////////
template <typename T>
void Swap(T& dataA, T& dataB)
{
	T data = dataA;
	dataA = dataB;
	dataB = data;
}